public class AssignmentOperatorExample {
    public static void main(String[] args) {
        int x = 5;
        System.out.println(x);
        x += 1; // 6
        System.out.println(x);
        x -=1; // 5
        System.out.println(x);
        x *= 2; // 10
        System.out.println(x);
        x /= 2; // 5
        System.out.println(x);
        x %= 2; // 1
        System.out.println(x);
    }
}
