public class RelationalOperatorExample {
    public static void main(String[] args) {
        int x = 5;
        int y = 6;
        System.out.println(x < y); // true
        System.out.println(x > y); // false
        System.out.println(x <= y); // true
        System.out.println(x >= y); // false
        System.out.println(x == y); // false
        System.out.println((x != y)); // true

        System.out.println((6 != 6)); //
    }
}
