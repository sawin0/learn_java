public class PrimitiveDataTypeExample {
    public static void main(String[] args) {
        char grade = 'A';
        grade = 'B';
        long count = 10000000000L;
        float gpa = 3.8f;
        double coordinates = 28.674563d;
        System.out.println("My Grade: " + grade);
        System.out.println("Count: " + count);
        System.out.println("My GPA: " + gpa);
        System.out.println("My Coordinates: " + coordinates);
    }
}
